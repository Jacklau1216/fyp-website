$(document).ready(function() {
    $("#detect-button").click(function() {
        var text = $("#text-input").val();
        $.ajax({
            url: "/detect",
            type: "POST",
            data: { text: text },
            success: function(response) {
                $("#result").text("Generated By AI: " + response);
            },
            error: function(xhr, status, error) {
                console.log(error);
            }
        });
    });
});